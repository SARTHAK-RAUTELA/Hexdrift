    // Add external Swiper script and CSS if not already added
    addScript() {
      if (document.querySelector("#cre-swiper-js")) return;  
      const scriptOne = document.createElement("script");
      scriptOne.id = "cre-swiper-js";
      scriptOne.src = "https://cdn.jsdelivr.net/npm/swiper@11/swiper-bundle.min.js"; 
      document.head.appendChild(scriptOne);

      if (document.querySelector("#cre-swiper-css")) return;  
      const swiperCss = `<link id="cre-swiper-css" rel="stylesheet" href="https://cdn.jsdelivr.net/npm/swiper@11/swiper-bundle.min.css"/>`; 
      document.head.insertAdjacentHTML("beforeend", swiperCss);
    },

    // Wait for Swiper to load and be available
    waitForSwiper(trigger) {
      const interval = setInterval(() => {
        if (typeof window.Swiper !== "undefined") {
          clearInterval(interval);
          trigger();  
        }
      }, 50);
      setTimeout(() => clearInterval(interval), 15000); 
    },

    // Create the Swiper slider and attach it to the page
   createSwiperSlider(images, targetElement) {
      const variation_name = this.__testName;

      if (document.querySelector(`.${variation_name}-slider-component`)) {
        return;  // Skip if slider already exists
      }

      // Generate HTML for slides and thumbnails
      const slidesHTML = images
        .map(image => `
          <div class="swiper-slide">
            <div class="${variation_name}-swiper-img-wrapper">
              <img src="${image.src}" productName="${image.alt}" />
            </div>
            <div class="${variation_name}-caption">
              <div class="${variation_name}-product">
                <div class="${variation_name}-product-text"> ${image.productName || ''}</div>
              </div>
              <div class="${variation_name}-caption-text"> ${image.caption || ''}</div>
            </div>
          </div>
        `).join("");

      const thumbsHTML = images
        .map(image => `
          <div class="swiper-slide">
            <img src="${image.src}" productName="${image.productName} thumbnail" />
          </div>
        `).join("");

      // Structure for the Swiper component (Main and Thumbs sliders)
      const sliderStructureHTML = `
        <div class="${variation_name}-slider-component">
          <div class="swiper main-swiper">
            <div class="swiper-wrapper">${slidesHTML}</div>
          </div>
          <div class="${variation_name}-swiper-thumbs">
            <div class="swiper thumbs-swiper">
              <div class="swiper-wrapper">${thumbsHTML}</div>
            </div>
            <div class="swiper-button-prev"></div>
            <div class="swiper-button-next"></div>
          </div>
        </div>
      `;

      // Insert the slider structure into the page
      this.runAt('.product-image-section', function() {
        targetElement.insertAdjacentHTML("beforeend", sliderStructureHTML);
      });

      // Initialize the thumbs Swiper
      const thumbsSwiper = new Swiper(targetElement.querySelector(".thumbs-swiper"), {
        spaceBetween: 10,
        slidesPerView: 5,
        freeMode: true,
        watchSlidesProgress: true,
        navigation: {
          nextEl: `.${variation_name}-swiper-thumbs .swiper-button-next`,
          prevEl: `.${variation_name}-swiper-thumbs .swiper-button-prev`,
        },
        breakpoints: {
          320: { 
            slidesPerView: 4, 
            spaceBetween: 15,
          },
          768: { 
            slidesPerView: 5,  
            spaceBetween: 20,
          }
        },
      });

      // Initialize the main Swiper with thumbs as navigation (No loop)
      const mainSwiper = new Swiper(targetElement.querySelector(".main-swiper"), {
        spaceBetween: 20,
        thumbs: {
          swiper: thumbsSwiper, 
        },
        navigation: {
          nextEl: `.${variation_name}-swiper-thumbs .swiper-button-next`,
          prevEl: `.${variation_name}-swiper-thumbs .swiper-button-prev`,
        },
        loop: false,  
        on: {
          slideChange: function () {
            const swiperInstance = this;
            const nextButton = swiperInstance.navigation.nextEl;
            const prevButton = swiperInstance.navigation.prevEl;
            
            // If at the first slide, disable the previous button
            if (swiperInstance.isBeginning) {
              prevButton.classList.add('swiper-button-disabled');
            } else {
              prevButton.classList.remove('swiper-button-disabled');
            }

            // If at the last slide, disable the next button
            if (swiperInstance.isEnd) {
              nextButton.classList.add('swiper-button-disabled');
            } else {
              nextButton.classList.remove('swiper-button-disabled');
            }
          },
        },
      });
    },

    // Initialize the configuration and create sliders
    init() {
      const $this = this;
      
      // Add the Swiper script and CSS
      $this.addScript();

      this.runAt('.product-image-section', function() {
        $this.waitForSwiper(() => {
          Object.values($this.config).forEach((configItems) => {
            configItems.forEach((item) => {
              const pageIdentifier = document.querySelector(item.dataPath);
              if (pageIdentifier) {
                const targetElement = pageIdentifier.querySelector(".product-image-section");
                if (targetElement) {
                  targetElement.classList.add(`${$this.__testName}-initialized`);
                  $this.createSwiperSlider(item.images, targetElement);
                }
              }
            });
          });
        });
      });
    }
